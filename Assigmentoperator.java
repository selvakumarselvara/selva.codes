class Assigmentoperator
{
public static void main(String args[])
{
int a=4;
int var;
var = a;
System.out.println("= variable is" +var);
var +=a;
System.out.println("+= variable is" +var);
var -=a;
System.out.println("-= variable is" +var);
var *=a;
System.out.println("*= variable is" +var);
var /=a;
System.out.println("/= variable is" +var);
var %=a;
System.out.println("%= variable is" +var);
}
}
